function dt = diabetes(t,y)
% diabetes model 

tmp1 = (2500*y(3)^2*(20*y(2) + 1/50))/(2500*y(3)^2 + 4) - y(1)^2 - y(1);
tmp2 = (4*y(1))/(5*(125000*y(3)^3 + 1)) - y(2)/100 - (5000*y(3)^2*y(2))/(2500*y(3)^2 + 4);
tmp3 = - (3*y(3))/10 - y(1)*(2/(25*(125000*y(3)^3 + 1)) - 1/10);
dt = [tmp1;tmp2;tmp3];

%{
---for deltaP = 1.5---
tmp1 = (10000*y(3)^2*(20*y(2) + 1/50))/(9*((10000*y(3)^2)/9 + 4)) - y(1)^2 - y(1);
tmp2 = (108*y(1))/(5000000*y(3)^3 + 135) - y(2)/100 - (5000*y(3)^2*y(2))/(2500*y(3)^2 + 9);
tmp3 = - (3*y(3))/10 - y(1)*(2/(25*((1000000*y(3)^3)/27 + 1)) - 1/10);
dt = [tmp1; tmp2; tmp3];
%}

end

